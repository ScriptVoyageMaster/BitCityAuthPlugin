package city.bit.auth.model;

/**
 * Перелік можливих станів гравця під час авторизації.
 * Це спрощена модель автомата станів, яка допомагає відстежувати,
 * на якому етапі взаємодії знаходиться користувач.
 */
public enum AuthState {
    UNAUTH,           // ще не увійшов
    AWAITING_INPUT,   // очікуємо введення пароля або його підтвердження
    AUTHENTICATED,    // успішно авторизований
    LOCKED            // тимчасово заблокований через багато невдалих спроб
}
